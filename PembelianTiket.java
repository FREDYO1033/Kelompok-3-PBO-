    private void txt_nomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_nomerActionPerformed
        // TODO add your handling code here:                                        
    // Mendapatkan teks dari txt_nomer
    String nomer = txt_nomer.getText();

    // Melakukan validasi sederhana: memeriksa apakah teks tidak kosong
    if(nomer.isEmpty()) {
        // Menampilkan pesan peringatan jika teks kosong
        JOptionPane.showMessageDialog(null, "Nomor tidak boleh kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
    } else {
        // Misalnya, menampilkan teks di label lain
        txt_nomer.setText("Nomor yang dimasukkan: " + nomer);
    }
    }//GEN-LAST:event_txt_nomerActionPerformed

