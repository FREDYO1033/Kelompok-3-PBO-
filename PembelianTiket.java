    private void jurusanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jurusanActionPerformed
        // TODO add your handling code here:
        if(jurusan.getSelectedItem().equals("Pilih Jurusan")){
        buttonGroup1.clearSelectedItem();
        txt_harga.setText("");
        }
        else if(jurusan.getSelectedItem().equals("Bekasi-Bandung")){
            vip=200000;
            ekonomi=80000;
        }
        else if(jurusan.getSelectedItem().equals("Bekasi-Jogya")){
            vip=300000;
            ekonomi=70000;
        }
        else if(jurusan.getSelectedItem().equals("Bekasi-Kebumen")){
            vip=150000;
            ekonomi=75000;
        }
        else if(jurusan.getSelectedItem().equals("Bekasi-Purwakerto")){
            vip=250000;
            ekonomi=125000;
        }
    }//GEN-LAST:event_jurusanActionPerformed

